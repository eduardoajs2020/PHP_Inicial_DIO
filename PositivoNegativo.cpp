#include <iostream>
		
	using namespace std;
	
	int main()
	
	{
		

		int numero;
		
	
		cout << "\n Informe um numero qualquer: ";
		cin >> numero;
	
			
		if(numero >0)
		{
			cout << "\n O numero " << numero << " e positivo!";
		}
		else if(numero<0)
		{
			cout <<  "\n O numero " << numero << " e negativo!";
		}
		
		else 
		{
			cout <<  "\n O numero " << numero << " e nulo!";
		}
		
}
	
		

